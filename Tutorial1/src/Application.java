public class Application {
//This is a "Hello World" and primitive data type program.
	public static void main(String[] args) {
		
		System.out.println("Hello World!");
		
		int myNumber;//Declaration
		
		myNumber=88;//Initialization
		
		//int myNumber=88; Declaration AND Initialization

		short myShort=347;//Declaration AND Initialization
		
		long myLong=873902;
		
		double myDouble=89.453;
		
		float myFloat=45.9f;
		
		char myChar='y';
		
		boolean myBoolean=true;
		
		byte myByte=127;
		

		System.out.println(myNumber);
		
		System.out.println(myShort);
		
		System.out.println(myLong);
		
		System.out.println(myDouble);
		
		System.out.println(myFloat);
		
		System.out.println(myChar);
		
		System.out.println(myBoolean);
		
		System.out.println(myByte);
	}
}

/*JAVA: 8 primitive types of variables**************************
 * 															   *
 * int-32 bit value (most whole numbers)					   *
 * short-16 bit value (smaller whole numbers)				   *	
 * long-64 bit value (really big whole numbers)				   *
 * double-double precision 64 bit floating point number	       *
 * float-short version of double 32 bit floating point number  *
 * char-16 bit unicode character (UTF-16)					   *
 * boolean-true or false (0 or 1)							   *
 * byte-8 bit value (-128 through 127)						   *
 * *************************************************************/
